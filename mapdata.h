// 获取所有关卡
inline std::vector<MapRowPtr> getAllMaps() {
    return {MAP_LEVEL1, MAP_LEVEL2, MAP_LEVEL3};
}

inline MapRowPtr getMap(int levelIndex) {
    auto maps = getAllMaps();
    if (levelIndex >= 0 && levelIndex < (int)maps.size())
        return maps[levelIndex];
    return MAP_LEVEL1;
}

// === 随机地图生成 ===
// level: 关卡索引（0起），密度随关卡递增
// density: ~18% + level*3%，上限 50%
// steelRatio: 约 25% 的墙是钢铁
inline void generateRandomMap(int map[MAP_ROWS][MAP_COLS], int level)
{
    // 清空
    for (int r = 0; r < MAP_ROWS; r++)
        for (int c = 0; c < MAP_COLS; c++)
            map[r][c] = CELL_EMPTY;

    // 墙壁密度随关卡递增
    float density = 0.18f + level * 0.03f;
    if (density > 0.50f) density = 0.50f;

    const float steelRatio = 0.25f;

    // 四周用钢铁围墙封住
    for (int c = 0; c < MAP_COLS; c++) {
        map[0][c] = CELL_STEEL;                      // 顶部边界
        map[MAP_ROWS - 1][c] = CELL_STEEL;           // 底部边界
    }
    for (int r = 0; r < MAP_ROWS; r++) {
        map[r][0] = CELL_STEEL;                      // 左侧边界
        map[r][MAP_COLS - 1] = CELL_STEEL;           // 右侧边界
    }

    // 出生点保护区
    const int clearTop    = 2;   // 敌人出生区（顶部）
    const int clearBottom = 3;   // 玩家出生区（底部）

    int centerCol = MAP_COLS / 2;

    for (int r = clearTop; r < MAP_ROWS - clearBottom; r++) {
        for (int c = 0; c < MAP_COLS; c++) {
            // 中央列密度略低，保证有路可走
            int distFromCenter = (c > centerCol) ? (c - centerCol) : (centerCol - c);
            float colFactor = (distFromCenter < 3)
                ? 0.5f + distFromCenter * 0.17f   // 中央 50%~84%
                : 1.0f;

            if ((rand() % 100) < (int)(density * colFactor * 100)) {
                if ((rand() % 100) < (int)(steelRatio * 100))
                    map[r][c] = CELL_STEEL;
                else
                    map[r][c] = CELL_BRICK;
            }
        }
    }
}

